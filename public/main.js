function myFunction() {
    var x = document.getElementById("mynavbarr");
    if (x.className === "navbarr") {
        x.className += " responsive";
        document.getElementById("myCarousel").style.marginTop = "340px";
        document.getElementById("slideshow-containerr").style.marginTop = "340px";


    } else {
        x.className = "navbarr";
        document.getElementById("myCarousel").style.marginTop = "0px";
        document.getElementById("slideshow-containerr").style.marginTop = "0px";


    }
}

